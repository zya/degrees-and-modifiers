// First coord is octaves, second is fifths. Distances are relative to c
var notes = {
  c: [0, 0],
  d: [-1, 2],
  e: [-2, 4],
  f: [1, -1],
  g: [0, 1],
  a: [-1, 3],
  b: [-2, 5],
  h: [-2, 5],
};

exports = function (name) {
  return name in notes ? [notes[name][0], notes[name][1]] : null;
};

exports.notes = notes;
exports.A4 = [3, 3]; // Relative to C0 (scientic notation, ~16.35Hz)
exports.sharp = [-4, 7];
